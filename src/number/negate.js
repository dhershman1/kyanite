/**
 * @name negate
 * @function
 * @since v0.10.0
 * @category Number
 * @sig Number -> Number
 * @description Negates the number passed in
 * @param {Number} n The number to negate
 * @returns {Number} The negated number
 * @example
 * import { negate } from 'kyanite'
 *
 * negate(1) // => -1
 * negate(-1) // => 1
 */
const negate = n => -n

export default negate
